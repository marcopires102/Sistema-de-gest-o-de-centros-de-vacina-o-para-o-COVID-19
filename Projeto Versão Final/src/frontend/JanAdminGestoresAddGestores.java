    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if(txtUsername.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Introduza o username do Gestor");
            txtUsername.requestFocus();
            return;
        }
        if(txtPassword.getPassword().length == 0){
            JOptionPane.showMessageDialog(this, "Introduza a password");
            txtPassword.requestFocus();
            return;
        }
        if(txtPassword2.getPassword().length == 0){
            JOptionPane.showMessageDialog(this, "Introduza a verifica√ßao da password");
            txtPassword2.requestFocus();
            return;
        }
        if(txtNome.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Introduza o nome do Gestor");
            txtNome.requestFocus();
            return;
        }
        String u = txtUsername.getText().trim();
        String p = new String(txtPassword.getPassword());
        String n = txtNome.getText().trim();
        if(funcao == 1){
            gestor.setNome(n);
            gestor.setUsername(u);
            gestor.setPassword(p);
        }
        else{
            if(sistema.getUtilizadores().existe(txtUsername.getText())){
                JOptionPane.showMessageDialog(this, "Ja existe um Gestor com esse codigo");
                txtUsername.requestFocus();
                return;
            }
            Gestor gestor = new Gestor(u, n, p);
            sistema.getUtilizadores().Adicionar(gestor);
        }
        dispose();
        JanAdminListGestores janela = new JanAdminListGestores(sistema, dados);
        janela.setVisible(true);
    }//GEN-LAST:event_btnAdicionarActionPerformed

